class Dataset_BTCUSDT_train(Dataset):
    """
    # 描述：
     需要仿照Dataset_ETT_hour类，实现一个能够读取BTCUSDT_1m_2025_delta.csv文件的类
     该文件包含了2025年1月1日到2025年1月21日的BTCUSDT的1分钟K线数据
     他的每一类分别是'Open_time', 'delta_open_price_ratio', 'delta_high_price_ratio', 'delta_low_price_ratio', 'delta_volume_ratio', 'delta_quote_asset_volume_ratio', 'delta_number_of_trades_ratio', 'delta_taker_buy_base_volume_ratio', 'delta_taker_buy_quote_volume_ratio', 'delta_close_price_ratio'
     其中，我们预期delta_close_price_ratio是我们的target, 而Open_time是一个单位为ms的时间戳

    """
    def __init__(self, root_path, flag='train', size=None,
                 features='MS', data_path='BTCUSDT_1m_2025_delta.csv',
                 target='delta_close_price_ratio', freq='1m', date_col='Open_time', timeenc=None, ):
        # size [seq_len, label_len, pred_len]
        # info
        if size == None:
            self.seq_len = 200
            self.label_len = 20
            self.pred_len = 1
        else:
            self.seq_len = size[0]
            self.label_len = size[1]
            self.pred_len = size[2]
        # init
        assert flag in ['train', 'test', 'val']
        type_map = {'train': 0, 'val': 1, 'test': 2}
        self.set_type = type_map[flag]
        self.features = features
        self.target = target
        self.freq = freq
        self.root_path = root_path
        self.data_path = data_path
        self.date_col = date_col
        self.__read_data__()
    
    def __read_data__(self):
        self.scaler = StandardScaler()
        df_raw = pd.read_csv(os.path.join(self.root_path,
                                          self.data_path))
        # df_raw.columns: ['Open_time', 'Open_price', 'High_price', 'Low_price', 'Close_price', 'Volume', 'Close_time', 'Quote_asset_volume', 'Number_of_trades', 'Taker_buy_base_volume', 'Taker_buy_quote_volume', 'Ignore']

        total_len = len(df_raw)
        # 7 : 2 : 1
        train_idx = int(total_len * 0.7)
        val_idx = int(total_len * 0.9)
        test_idx = total_len
        border1s = [0, train_idx - self.seq_len, val_idx - self.seq_len]
        border2s = [train_idx, val_idx, test_idx]
        border1 = border1s[self.set_type]
        border2 = border2s[self.set_type]
        if self.features == 'M' or self.features == 'MS':
            cols_data = df_raw.columns[1:]
            df_data = df_raw[cols_data]
        elif self.features == 'S':
            df_data = df_raw[[self.target]]

        data = df_data.values
        df_stamp = df_raw[[self.date_col]][border1:border2]
        df_stamp[self.date_col] = pd.to_datetime(df_stamp[self.date_col])
        df_stamp['month'] = df_stamp[self.date_col].apply(lambda row: row.month, 1)
        df_stamp['day'] = df_stamp[self.date_col].apply(lambda row: row.day, 1)
        df_stamp['weekday'] = df_stamp[self.date_col].apply(lambda row: row.weekday(), 1)
        df_stamp['hour'] = df_stamp[self.date_col].apply(lambda row: row.hour, 1)
        data_stamp = df_stamp.drop(columns=[self.date_col], axis=1).values
        self.data_x = data[border1:border2]
        self.data_y = data[border1:border2]
        self.data_stamp = data_stamp

    def __getitem__(self, index):
        s_begin = index
        s_end = s_begin + self.seq_len
        r_begin = s_end - self.label_len
        r_end = r_begin + self.label_len + self.pred_len

        seq_x = self.data_x[s_begin:s_end]
        seq_y = self.data_y[r_begin:r_end]
        seq_x_mark = self.data_stamp[s_begin:s_end]
        seq_y_mark = self.data_stamp[r_begin:r_end]

        return seq_x, seq_y, seq_x_mark, seq_y_mark

    def __len__(self):
        return len(self.data_x) - self.seq_len - self.pred_len + 1

    def inverse_transform(self, data):
        return data

class Dataset_ETT_hour(Dataset):
    def __init__(self, root_path, flag='train', size=None,
                 features='S', data_path='ETTh1.csv',
                 target='OT', scale=True, timeenc=0, freq='h'):
        # size [seq_len, label_len, pred_len]
        # info
        if size == None:
            self.seq_len = 24 * 4 * 4
            self.label_len = 24 * 4
            self.pred_len = 24 * 4
        else:
            self.seq_len = size[0]
            self.label_len = size[1]
            self.pred_len = size[2]
        # init
        assert flag in ['train', 'test', 'val']
        type_map = {'train': 0, 'val': 1, 'test': 2}
        self.set_type = type_map[flag]

        self.features = features
        self.target = target
        self.scale = scale
        self.timeenc = timeenc
        self.freq = freq

        self.root_path = root_path
        self.data_path = data_path
        self.__read_data__()

    def __read_data__(self):
        self.scaler = StandardScaler()
        df_raw = pd.read_csv(os.path.join(self.root_path,
                                          self.data_path))

        border1s = [0, 12 * 30 * 24 - self.seq_len, 12 * 30 * 24 + 4 * 30 * 24 - self.seq_len]
        border2s = [12 * 30 * 24, 12 * 30 * 24 + 4 * 30 * 24, 12 * 30 * 24 + 8 * 30 * 24]
        border1 = border1s[self.set_type]
        border2 = border2s[self.set_type]

        if self.features == 'M' or self.features == 'MS':
            cols_data = df_raw.columns[1:]
            df_data = df_raw[cols_data]
        elif self.features == 'S':
            df_data = df_raw[[self.target]]

        if self.scale:
            train_data = df_data[border1s[0]:border2s[0]]
            self.scaler.fit(train_data.values)
            data = self.scaler.transform(df_data.values)
        else:
            data = df_data.values

        df_stamp = df_raw[['date']][border1:border2]
        df_stamp['date'] = pd.to_datetime(df_stamp.date)
        if self.timeenc == 0:
            df_stamp['month'] = df_stamp.date.apply(lambda row: row.month, 1)
            df_stamp['day'] = df_stamp.date.apply(lambda row: row.day, 1)
            df_stamp['weekday'] = df_stamp.date.apply(lambda row: row.weekday(), 1)
            df_stamp['hour'] = df_stamp.date.apply(lambda row: row.hour, 1)
            data_stamp = df_stamp.drop(['date'], 1).values
        elif self.timeenc == 1:
            data_stamp = time_features(pd.to_datetime(df_stamp['date'].values), freq=self.freq)
            data_stamp = data_stamp.transpose(1, 0)

        self.data_x = data[border1:border2]
        self.data_y = data[border1:border2]
        self.data_stamp = data_stamp

    def __getitem__(self, index):
        s_begin = index
        s_end = s_begin + self.seq_len
        r_begin = s_end - self.label_len
        r_end = r_begin + self.label_len + self.pred_len

        seq_x = self.data_x[s_begin:s_end]
        seq_y = self.data_y[r_begin:r_end]
        seq_x_mark = self.data_stamp[s_begin:s_end]
        seq_y_mark = self.data_stamp[r_begin:r_end]

        return seq_x, seq_y, seq_x_mark, seq_y_mark

    def __len__(self):
        return len(self.data_x) - self.seq_len - self.pred_len + 1

    def inverse_transform(self, data):
        return self.scaler.inverse_transform(data)


